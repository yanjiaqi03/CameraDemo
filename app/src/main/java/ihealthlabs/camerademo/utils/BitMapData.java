package ihealthlabs.camerademo.utils;

public class BitMapData {
	private int width = 0;
	private int height = 0;
	
	public void setWidth(int width) {
		this.width = width;
	}
	
	public int getWidth() {
		return width;
	}
	
	public void setHeight(int height) {
		this.height = height;
	}
	
	public int getHeight() {
		return height;
	}
}
